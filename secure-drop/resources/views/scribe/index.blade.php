                    <h2 id="secrets-GETapi-v1-secrets--id-">Retrieve and delete the specified secret.</h2>

<p>
</p>

<p>Retrieves the secret content and immediately deletes it from the database.
This endpoint can only be called once per secret (burn on read).</p>

<span id="example-requests-GETapi-v1-secrets--id-">
<blockquote>Example request:</blockquote>


<div class="bash-example">
    <pre><code class="language-bash">curl --request GET \
    --get "http://localhost:8000/api/v1/secrets/9d4e5f6a-7b8c-9d0e-1f2a-3b4c5d6e7f8a" \
    --header "Content-Type: application/json" \
    --header "Accept: application/json"</code></pre></div>


<div class="javascript-example">
    <pre><code class="language-javascript">const url = new URL(
