                                <h2 id="secrets-POSTapi-v1-secrets">Store a newly created secret.</h2>

<p>
</p>

<p>Creates a new secret with optional TTL. Returns a unique URL that can be shared.
Once accessed, the secret is permanently deleted (burn on read).</p>

<span id="example-requests-POSTapi-v1-secrets">
<blockquote>Example request:</blockquote>


<div class="bash-example">
    <pre><code class="language-bash">curl --request POST \
    "http://localhost:8000/api/v1/secrets" \
    --header "Content-Type: application/json" \
    --header "Accept: application/json" \
    --data "{
    \"content\": \"MySecretPassword123\",
    \"ttl_minutes\": 60
}"
</code></pre></div>


<div class="javascript-example">
    <pre><code class="language-javascript">const url = new URL(
    "http://localhost:8000/api/v1/secrets"
);

const headers = {
    "Content-Type": "application/json",
    "Accept": "application/json",
};

let body = {
    "content": "MySecretPassword123",
    "ttl_minutes": 60
};

fetch(url, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
}).then(response =&gt; response.json());</code></pre></div>

